CREATE DATABASE IF NOT EXISTS TokoSuper;
USE TokoSuper;

DROP TABLE IF EXISTS Provinsi;
CREATE TABLE Provinsi (
	ProvinsiId int unsigned NOT NULL AUTO_INCREMENT,
	ProvinsiNama varchar(100),
	PRIMARY KEY(ProvinsiId)
);

DROP TABLE IF EXISTS Pelanggan;
CREATE TABLE Pelanggan (
	PelangganId int unsigned NOT NULL AUTO_INCREMENT,
	PelangganNama varchar(100),
	ProvinsiId int unsigned NOT NULL,
	PRIMARY KEY(PelangganId),
	FOREIGN KEY (ProvinsiId) REFERENCES Provinsi(ProvinsiId)
);

DROP TABLE IF EXISTS Vendor;
CREATE TABLE Vendor (
	VendorId int unsigned NOT NULL AUTO_INCREMENT,
	VendorNama varchar(100),
	ProvinsiId int unsigned NOT NULL,
	PRIMARY KEY(VendorId),
	FOREIGN KEY (ProvinsiId) REFERENCES Provinsi(ProvinsiId)
);


DROP TABLE IF EXISTS Produk;
CREATE TABLE Produk (
	ProdukId int unsigned NOT NULL AUTO_INCREMENT,
	VendorId int unsigned NOT NULL,
	ProdukNama varchar(100),
	ProdukHargaBeli int unsigned NOT NULL,
	ProdukHargaJual int unsigned NOT NULL,
	PRIMARY KEY(ProdukId),
	FOREIGN KEY (VendorId) REFERENCES Vendor(VendorId)
);

DROP TABLE IF EXISTS Penjualan;
CREATE TABLE Penjualan (
	PenjualanId int unsigned NOT NULL AUTO_INCREMENT,
	ProdukId int unsigned NOT NULL,
	PelangganId int unsigned NOT NULL,
	PenjualanJumlah int unsigned NOT NULL,
	PRIMARY KEY(PenjualanId),
	FOREIGN KEY (ProdukId) REFERENCES Produk(ProdukId),
	FOREIGN KEY (PelangganId) REFERENCES Pelanggan(PelangganId)
);

